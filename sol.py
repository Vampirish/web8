app.config['UPLOAD_FOLDER'] = UPLOAD_FOLDER

last_photo = None


@app.route('/load_photo', methods=['POST', 'GET'])
def load_photo():
    global last_photo
    if request.method == 'GET':
        if last_photo is not None:
            img = f"""<img src={url_for('static', filename=f'uploads/{last_photo}')} width=80% height=100%>"""
        return f'''<!doctype html>
                        <html lang="en">
                          <head>
                            <meta charset="utf-8">
                            <meta name="viewport" content="width=device-width, initial-scale=1, shrink-to-fit=no">
                            <link rel="stylesheet"
                            href="https://stackpath.bootstrapcdn.com/bootstrap/4.4.1/css/bootstrap.min.css"
                            integrity="sha384-Vkoo8x4CGsO3+Hhxv8T/Q5PaXtkKtu6ug5TOeNV6gBiFeWPGFN9MuhOf23Q9Ifjh"
                            crossorigin="anonymous">
                            <link rel="stylesheet" type="text/css" href="{url_for('static', filename='css/style.css')}" />
                            <title>Отбор астронавтов</title>
                          </head>
                          <body>
                            <center>
                            <h1>Загрузка фотографии</h1>
                            <h2>для участия в миссии</h2>
                            </center>
                            <div>
                              <form class="login_form" method="post"enctype="multipart/form-data">
                               <label>Приложите фотографию</label>
                               <div class="photo">
                                    <input type='file' accept='image/*' name='photo'>  
                                </div>
                                <div>
                                  {img if last_photo else ''}
                                </div>
                                <button type="submit" class="btn btn-primary">Отправить</button>
                              </form>
                            </div>
                          </body>
                        </html>'''
    elif request.method == 'POST':
        file = request.files['photo']
        filename = utils.secure_filename(file.filename)
        last_photo = filename.rsplit('.', maxsplit=1)[0] + '.jpg'
        img = Image.open(BytesIO(file.read()))
        img.save(os.path.join(app.config["UPLOAD_FOLDER"], last_photo))
        return "Photo saved"


